<h1>Introduction to Trongate MX</h1>
<p>Trongate MX is a JavaScript library inspired by HTMX but specifically designed for the Trongate framework. It enables developers to implement advanced JavaScript functionality without writing any JavaScript code, making web development more accessible and efficient.</p>
<h2>Key Features</h2>
<ul>
  <li>Seamless integration with Trongate framework</li>
  <li>HTMX-like functionality tailored for Trongate</li>
  <li>Zero JavaScript coding required for complex interactions</li>
  <li>Designed with Trongate Pages in mind</li>
</ul>
<h2>Why Trongate MX?</h2>
<p>While HTMX is an excellent library that inspired Trongate MX, there are two primary reasons for developing this alternative:</p>
<ol>
  <li>
    <strong>Framework Stability:</strong> With HTMX moving to version 2, integrating it into the core of Trongate would compromise the framework's stability. Trongate MX allows us to maintain the stability that Trongate prioritizes while providing similar functionality.
  </li>
  <li>
    <strong>Trongate Pages Integration:</strong> Trongate Pages, the pre-installed content management system in the Trongate framework, requires specific HTMX-like functionality to reach its full potential. Trongate MX is tailored to work seamlessly with Trongate Pages, aiming to make it one of the best content management systems in web development.
  </li>
</ol>
<h2>Relationship with HTMX</h2>
<p>It's important to note that Trongate MX is not intended to compete with or replace HTMX. While every line of Trongate MX was written from scratch for Trongate, the similarities with HTMX are intentional and acknowledged. HTMX remains an excellent library, and Trongate MX simply provides an alternative specifically designed for the Trongate ecosystem.</p>
<h2>Getting Started</h2>
<p>Trongate MX comes pre-installed with the Trongate framework, allowing you to start using its features immediately. In the following sections, we'll explore how to use Trongate MX attributes to add dynamic behaviors to your web pages without writing JavaScript.</p>
<p>Whether you're building a simple website or a complex web application, Trongate MX provides the tools you need to create interactive, responsive user interfaces with ease.</p>