<h1>Trongate MX Attribute Reference</h1>
<p>This page provides a quick reference for all Trongate MX attributes.</p>
<table>
  <thead>
    <tr>
      <th>Attribute</th>
      <th>Description</th>
    </tr>
  </thead>
  <tbody>
    <tr>
      <td>
        <code>mx-get</code>
      </td>
      <td>Issues a GET request to the specified URL</td>
    </tr>
    <tr>
      <td>
        <code>mx-post</code>
      </td>
      <td>Issues a POST request to the specified URL</td>
    </tr>
    <tr>
      <td>
        <code>mx-put</code>
      </td>
      <td>Issues a PUT request to the specified URL</td>
    </tr>
    <tr>
      <td>
        <code>mx-patch</code>
      </td>
      <td>Issues a PATCH request to the specified URL</td>
    </tr>
    <tr>
      <td>
        <code>mx-delete</code>
      </td>
      <td>Issues a DELETE request to the specified URL</td>
    </tr>
    <tr>
      <td>
        <code>mx-load</code>
      </td>
      <td>Automatically loads content when the element is added to the DOM</td>
    </tr>
    <tr>
      <td>
        <code>mx-select</code>
      </td>
      <td>Selects content to swap in from a response</td>
    </tr>
    <tr>
      <td>
        <code>mx-select-oob</code>
      </td>
      <td>Selects content to swap in from a response, outside the target (out of band)</td>
    </tr>
    <tr>
      <td>
        <code>mx-swap</code>
      </td>
      <td>Controls how content will be swapped in (innerHTML, outerHTML, beforebegin, etc.)</td>
    </tr>
    <tr>
      <td>
        <code>mx-trigger</code>
      </td>
      <td>Specifies the event that triggers the request</td>
    </tr>
    <tr>
      <td>
        <code>mx-target</code>
      </td>
      <td>Specifies the target element to be swapped</td>
    </tr>
    <tr>
      <td>
        <code>mx-indicator</code>
      </td>
      <td>Specifies an element to show as a loading indicator during the request</td>
    </tr>
  </tbody>
</table>
<p>For detailed information on each attribute, please refer to the individual documentation pages.</p>